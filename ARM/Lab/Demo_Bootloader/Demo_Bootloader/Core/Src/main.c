void jump_to_user_app()
{
	printf("Jump to UserApplication\n");
	code_for_jump_to_user_app();
}

void code_for_jump_to_user_app()
{
	//just a function pointer to hold the address of the reset handler of the user app.
	    void (*app_reset_handler)(void);

	    printf("Jump to UserApplication: Process Begin\n");


	    // 1. configure the MSP by reading the value from the base address of the sector 2
	    uint32_t msp_value = *(volatile uint32_t *)FLASH_SECTOR2_BASE_ADDRESS;
	    printf("Setting Main Stack Pointer:MSP value : %#x\n",msp_value);

	    //This function comes from CMSIS.
	    __set_MSP(msp_value);

	    //SCB->VTOR = FLASH_SECTOR2_BASE_ADDRESS;

	    /* 2. Now fetch the reset handler address of the user application
	     * from the location RESET_HANDLER_AT_ADDRESS=FLASH_SECTOR2_BASE_ADDRESS+4
	     */
	    uint32_t resethandler_address = *(volatile uint32_t *) (RESET_HANDLER_AT_ADDRESS);

	    app_reset_handler = (void*) resethandler_address;

	    //3. jump to reset handler of the user application
	    app_reset_handler();
}
